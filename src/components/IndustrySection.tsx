export default function IndustrySection() {
  const { t } = useLanguage();
  const pathname = usePathname();
  const [currentPrefix, setCurrentPrefix] = useState('');

  useEffect(() => {
    // Determine language prefix from pathname
    if (pathname.startsWith('/zh')) {
      setCurrentPrefix('/zh');
    } else {
      setCurrentPrefix('/en');
    }
  }, [pathname]);

  // Function to create language-aware links
  const createLink = (path: string) => {
    // During initial render, use the path directly to avoid hydration mismatch
    if (!currentPrefix) {
      return path;
    }
    return `${currentPrefix}${path}`;
  };
  
  return (
    <section className="py-16 bg-white">
      <div className="container mx-auto px-4">
        <div className="text-center mb-12">
          <h2 className="text-3xl font-bold text-gray-900 mb-4">{t('home.industries.title')}</h2>
          <p className="text-gray-600 max-w-2xl mx-auto">
            {t('home.industries.subtitle')}
          </p>
        </div>
        
        <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-3 gap-8">
          {industries.map((industry) => (
            <Link 
              key={industry.id}
              href={createLink(`/industries/${industry.id}`)}
              className="bg-gray-50 rounded-lg p-6 transition-all hover:shadow-md hover:bg-blue-50"
            >
              <div className="mb-4">
                {industry.icon}
              </div>
              <h3 className="text-xl font-semibold text-gray-900 mb-2">
                {t(`home.industries.items.${industry.id}.name`)}
              </h3>
              <p className="text-gray-600 mb-4">
                {t(`home.industries.items.${industry.id}.description`)}
              </p>
              <span className="text-blue-600 font-medium inline-flex items-center">
                {t('common.learnMore')}
                <svg xmlns="http://www.w3.org/2000/svg" className="h-4 w-4 ml-1" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 5l7 7-7 7" />
                </svg>
              </span>
            </Link>
          ))}
        </div>
      </div>
    </section>
  );
} 